	public String getLottos(String s){
		String str="";
		//공백으로 원하는 문자를 자른다.
		java.util.StringTokenizer st=new java.util.StringTokenizer(s);
		st.nextToken();//첫 번째 것은 받지 않는다. 
		for(int i=0;i<7;i++){
			str+=st.nextToken()+" ";//7개의 단어를 받는다.
		}
		return str;
	}
	public int to10(String s){
		int aa=0;
		String ss=s;
		if(s.charAt(0)=='0'){  //첫번 째 문자가 0인가?
			ss=s.replace('0',' '); //0을 공백으로 변화 시켜라 
		}
		aa=Integer.parseInt(ss.trim());  //공백 제거후 int로 변환
		return aa;
	}
	
	public int[] toInt(String s){
		int [] aa=new int[7];
		//공백으로 스트링 구분 
		java.util.StringTokenizer st=new java.util.StringTokenizer(s);
		//구분시킨 스트링을 숫자로 바꾸어서 배열에 저장
		for(int i=0;st.hasMoreTokens();i++){
			aa[i]=to10(st.nextToken().trim());
		}
		return aa;
	}
	
	
